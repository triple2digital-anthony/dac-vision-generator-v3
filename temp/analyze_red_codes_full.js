// Clean up the codes by removing trailing spaces
const codes = redCodes.split('\n').map(code => code.trim());
const prefixes = {};
const firstChars = {};
const prefixesByFirstChar = { '6': [], '7': [], 'v': [] };

// Count prefixes (first 3 characters)
codes.forEach(code => {
  const prefix = code.substring(0, 3);
  prefixes[prefix] = (prefixes[prefix] || 0) + 1;
  
  const firstChar = code.charAt(0);
  firstChars[firstChar] = (firstChars[firstChar] || 0) + 1;
  
  // Add prefix to the appropriate list if not already there
  if (!prefixesByFirstChar[firstChar].includes(prefix)) {
    prefixesByFirstChar[firstChar].push(prefix);
  }
});

// Sort prefixes by frequency
const sortedPrefixes = Object.entries(prefixes)
  .sort((a, b) => b[1] - a[1])
  .map(([prefix, count]) => `${prefix}: ${count} (${(count / codes.length * 100).toFixed(1)}%)`);

// Calculate first character distribution
const totalCodes = codes.length;
const firstCharDist = Object.entries(firstChars)
  .sort((a, b) => b[1] - a[1])
  .map(([char, count]) => `${char}: ${count} (${(count / totalCodes * 100).toFixed(1)}%)`);

// Get top prefixes (those with more than 1 occurrence)
const topPrefixes = Object.entries(prefixes)
  .filter(([_, count]) => count > 1)
  .sort((a, b) => b[1] - a[1])
  .map(([prefix, _]) => prefix);

